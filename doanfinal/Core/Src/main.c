void rotary_motor(uint8_t add, int16_t value)
{
    if(add == 1)
    {
      if(value<=0)
      {
        htim3.Instance->CCR4 = -value;
        HAL_GPIO_WritePin(GPIOB,GPIO_PIN_13,GPIO_PIN_SET);
        HAL_GPIO_WritePin(GPIOB,GPIO_PIN_14,GPIO_PIN_RESET);
      }
      else 
      {
        htim3.Instance->CCR4 = value;
        HAL_GPIO_WritePin(GPIOB,GPIO_PIN_13,GPIO_PIN_RESET);
        HAL_GPIO_WritePin(GPIOB,GPIO_PIN_14,GPIO_PIN_SET);
      }
    }
    else if(add == 2)
    {
      if(value<=0)
      {
        htim2.Instance->CCR3 = -value;
        HAL_GPIO_WritePin(GPIOB,GPIO_PIN_3,GPIO_PIN_RESET);
        HAL_GPIO_WritePin(GPIOD,GPIO_PIN_6,GPIO_PIN_SET);
      }
      else 
      {
        htim2.Instance->CCR3 = value;
        HAL_GPIO_WritePin(GPIOB,GPIO_PIN_3,GPIO_PIN_SET);
        HAL_GPIO_WritePin(GPIOD,GPIO_PIN_6,GPIO_PIN_RESET);
      }
    }
}


void rotary(int16_t value)
{
  if(value<0)
  {
    Robot.rotary.speed.wheel[0]=-0;
    Robot.rotary.speed.wheel[1]=-0;
    Robot.rotary.speed.wheel[2]=-0;
    Robot.rotary.speed.wheel[3]=0;
  }
  if(value>0)
  {
    Robot.rotary.speed.wheel[0]=0;
    Robot.rotary.speed.wheel[1]=0;
    Robot.rotary.speed.wheel[2]=0;
    Robot.rotary.speed.wheel[3]=0;
  }
}

